
public class Vehicle
{
	String type;
	int wheels;
	boolean isFast;


	public Vehicle(String t, int w, boolean f){

		type = t;
		wheels = w;
		isFast = f;
	}
	public String getType(){

		return type;

	}
	public int getWheels(){

		return wheels;

	}

	public boolean getIsFast(){

		return isFast;


	}



}
